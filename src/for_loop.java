import java.util.Scanner;

public class for_loop {

	public static void main(String[] args) {
		
		int num;
		System.out.print("ENTER NUMBER=");
		Scanner v=new Scanner (System.in);
          num=v.nextInt();
          
          for(int i=1; i<=10;i++ )
          {
        	  System.out.println(num*i);
          }          
	}
}


